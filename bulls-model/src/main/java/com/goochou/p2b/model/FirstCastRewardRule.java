package com.goochou.p2b.model;

public class FirstCastRewardRule {
	
	private Double amount;
	
	private Double inviterReward;
	
	private Double friendAward;

	public Double getAmount() {
		return amount;
	}

	public void setAmount(Double amount) {
		this.amount = amount;
	}

	public Double getInviterReward() {
		return inviterReward;
	}

	public void setInviterReward(Double inviterReward) {
		this.inviterReward = inviterReward;
	}

	public Double getFriendAward() {
		return friendAward;
	}

	public void setFriendAward(Double friendAward) {
		this.friendAward = friendAward;
	}
}
